# Strings
#   - A string is a sequence of characters
#   - They can be defined using single, double or tripple quotes
#   - Characters can be:
#     - Alphabets (capital and small)
#     - Digits (0 - 9)
#     - Whitespace (space)
#     - Special Characters (@, #, $ ......)

a = 'String with single Quotes'
b = "String with Double Quotes, in double quotes we can use single quote's like this"
c = '''
      String with Tripple Quotes,
      in tripple quotes we can use single and double quote 's "s like this
      and we can use multiline content
    '''

print(type(a))
print(type(b))
print(type(c))
print(c)