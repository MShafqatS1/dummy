print("Here starting point is zero and by default it will jump by 1")
print(list(range(5)))

print("we can change starting value to like 1")
print(list(range(1, 5)))

print("we can change jump value to like 2")
print(list(range(1, 10, 2)))

print("we can print reverse numbers also")
print(list(range(10, 1, -1)))

print("we can print reverse numbers with jump step 2")
print(list(range(10, 1, -2)))

print("We can also combine negative numbers and positive numbers")
print(list(range(-5, 5)))